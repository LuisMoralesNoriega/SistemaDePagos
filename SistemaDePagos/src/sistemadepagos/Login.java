    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String usuario = jTextField1.getText();
        String contra = jTextField2.getText();
        
        int login = tra.SelectLogin(usuario, contra);
        
        if(login == 1){
            Principal prin = new Principal();
            prin.setLocationRelativeTo(null);
            prin.setVisible(true);
            this.dispose();
        }else{
            jTextField1.setText("");
            jTextField2.setText("");
            jLabel5.setText("Usuario o Contrasenia Incorrecta!!");
            JOptionPane.showMessageDialog(this, "Usuario o Contrasenia Incorrecta!!", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }       
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object clave = "cooperativo";
        
        String seleccion = JOptionPane.showInputDialog(this,"Clave de Institucion",JOptionPane.QUESTION_MESSAGE);
        
        if(seleccion.equals(clave)){
            Registro reg = new Registro(tra);
            reg.setLocationRelativeTo(null);
            reg.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "No puede Registrarse no es la clave correcta!", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

